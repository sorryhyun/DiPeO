    if node_type == NodeType.TYPESCRIPT_AST:
        return TypescriptAstNode(
            id=node_id,
            position=position,
            label=label,
            flipped=flipped,
            metadata=metadata,
            source=data.get("source", None),
            extractPatterns=data.get("extractPatterns", None),
            includeJSDoc=data.get("includeJSDoc", None),
            parseMode=data.get("parseMode", None),
        )
    

    if node_type == NodeType.SUB_DIAGRAM:
        return SubDiagramNode(
            id=node_id,
            position=position,
            label=label,
            flipped=flipped,
            metadata=metadata,
            diagram_name=data.get("diagram_name", None),
            diagram_format=_to_enum(data.get("diagram_format", None), DiagramFormat),
            diagram_data=data.get("diagram_data", None),
            batch=data.get("batch", None),
            batch_input_key=data.get("batch_input_key", None),
            batch_parallel=data.get("batch_parallel", None),
            ignoreIfSub=data.get("ignoreIfSub", None),
        )
    

    if node_type == NodeType.INTEGRATED_API:
        return IntegratedApiNode(
            id=node_id,
            position=position,
            label=label,
            flipped=flipped,
            metadata=metadata,
            provider=data.get("provider"),
            operation=data.get("operation"),
            config=data.get("config", None),
            resource_id=data.get("resource_id", None),
            timeout=data.get("timeout", None),
            max_retries=data.get("max_retries", None),
        )
    

    if node_type == NodeType.PERSON_BATCH_JOB:
        return PersonBatchJobNode(
            id=node_id,
            position=position,
            label=label,
            flipped=flipped,
            metadata=metadata,
        )
    
    raise ValueError(f"Unknown node type: {node_type}")